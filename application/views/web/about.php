                <h4>What is Afyadata?</h4>

                <p class="text-justify">Afyadata is a tool that analyzes all the data collected from the field and
                    intelligently sends feedback to the data collector and sends an alert to higher authority officials
                    if any abnormal pattern is discovered in the data collected, this tool provides a graphical user
                    interface for involved health stakeholders to analyze and visualizing data collected via Afyadata
                    mobile app for android.</p>

                <h4>How does AfyaData Manager works?</h4>
                <p class="text-justify">
                    This application works together with the android AfyaData App, where you use
                    the mobile app to collect data in the field and later on you send them to the server
                    (AfyaData Manager application) for further processing.</p>
                <p class="text-justify">When the data gets into the server it is analyzed immediately using the natural
                    intelligence build in to detect any indicators
                    of abnormal pattern, if found an alert is sent immediately to respective health
                    experts / response team and sending tips back to the data collector while further actions are
                    to be taken.
                </p>


                <h4>AfyaData Mobile</h4>
                <p class="text-justify">This is open source tools for collecting, submitting data from health facilities
                    to the main server and receiving feedback from main server.</p>
                <p class="text-justify">This customized version of ODK Collect which have best form management modules.
                    This app works together with server side which accepts
                    data, storing to the database and analyzing the database for making decision from submitted
                    data.</p>

                <h4>How does AfyaData Mobile works?</>
                <p class="text-justify">The app works as a client, where by users from field can collect and submit
                    data to the central server via Internet connection as well as receiving feedback of submitted
                    data from the server/experts. </p>
                <p class="text-justify">While collecting data, phone can be offline without affecting
                    the process also has a capability of including GPS location and photo to a collected data.
                </p>


